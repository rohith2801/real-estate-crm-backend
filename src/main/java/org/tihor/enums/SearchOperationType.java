package org.tihor.enums;

/**
 * The enum Search operation type.
 */
public enum SearchOperationType {
    /**
     * The Match.
     */
    MATCH,
    /**
     * The Match start.
     */
    MATCH_START,
    /**
     * The Match end.
     */
    MATCH_END,
    /**
     * The Greater than.
     */
    GREATER_THAN,
    /**
     * The Less than.
     */
    LESS_THAN,
    /**
     * The Greater than equal.
     */
    GREATER_THAN_EQUAL,
    /**
     * The Less than equal.
     */
    LESS_THAN_EQUAL,
    /**
     * The Not equal.
     */
    NOT_EQUAL,
    /**
     * The Equal.
     */
    EQUAL,
    /**
     * The In.
     */
    IN,
    /**
     * The Not in.
     */
    NOT_IN
}
